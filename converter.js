exports.convertLatinToCyrillic = (text) => {
  const map = {
    // Double letters (must be replaced first)
    Nj: "Њ",
    NJ: "Њ",
    nj: "њ",
    Dž: "Џ",
    DŽ: "Џ",
    dž: "џ",
    Lj: "Љ",
    LJ: "Љ",
    lj: "љ",

    // Single letters
    A: "А",
    a: "а",
    B: "Б",
    b: "б",
    V: "В",
    v: "в",
    G: "Г",
    g: "г",
    D: "Д",
    d: "д",
    Đ: "Ђ",
    đ: "ђ",
    E: "Е",
    e: "е",
    Ž: "Ж",
    ž: "ж",
    Z: "З",
    z: "з",
    I: "И",
    i: "и",
    J: "Ј",
    j: "ј",
    K: "К",
    k: "к",
    L: "Л",
    l: "л",
    M: "М",
    m: "м",
    N: "Н",
    n: "н",
    O: "О",
    o: "о",
    P: "П",
    p: "п",
    R: "Р",
    r: "р",
    S: "С",
    s: "с",
    T: "Т",
    t: "т",
    Ć: "Ћ",
    ć: "ћ",
    U: "У",
    u: "у",
    F: "Ф",
    f: "ф",
    H: "Х",
    h: "х",
    C: "Ц",
    c: "ц",
    Č: "Ч",
    č: "ч",
    Š: "Ш",
    š: "ш",
  };

  // Use regex to replace text efficiently
  return text.replace(
    /Nj|NJ|nj|Dž|DŽ|dž|Lj|LJ|lj|[A-Za-zĆćĐđŽžČčŠš]/g,
    (match) => map[match] || match
  );
};
